package Universidad.RPA;

public class puntoCuatro {
    public static void main(String[] args) {
        System.out.println("¡¡Receta de Crumble de Manzana!!");
        System.out.println("Conseguir los siguientes ingredientes y utensillos:");
        System.out.println("Listado: 4 Manzanas, 150gr de Harina, Azucar y Manteca (la cantidad puede variar un poco), un bowl grande, un molde para tortas mediano/grande y un cornet.");
        System.out.println("Realizar los siguientes pasos:");
        System.out.println("1°Enmantecar la base y laterales del molde.");
        System.out.println("2°Lavar las manzanas, luego pelarlas y cortarlas en rodajas finas de 3mm a 2mm.");
        System.out.println("3°Encender el horno y ajustar la temperatura a 200 grados Celcius.");
        System.out.println("3°Mezclar utilizando un cornet: la harina, el azucar y la manteca fría, hasta conseguir una textura arenoza.");
        System.out.println("4°Colocar las rodajas de manzanas en la base del molde y cubrir las manzanas con el arenado.");
        System.out.println("5°Llevar el crumble al horno, cocinar por media hora a 200 grados Celcius aproximadamente.");
        System.out.println("6°Retirar del horno, dejar enfriar 10 minutos o hasta que este templado y servir.");
        System.out.println("7°Opcionalmente guardar en la heladera y esperar una noche para comerlo frio.");
    }
}
